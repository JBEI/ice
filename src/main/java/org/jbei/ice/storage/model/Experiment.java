package org.jbei.ice.storage.model;

import jakarta.persistence.*;
import org.hibernate.annotations.LazyCollection;
import org.hibernate.annotations.LazyCollectionOption;
import org.jbei.ice.experiment.Study;
import org.jbei.ice.storage.DataModel;

import java.util.Date;
import java.util.LinkedHashSet;
import java.util.Set;

/**
 * Storage entity for experimental data
 *
 * @author Hector Plahar
 */
@Entity
@Table(name = "experiment")
@SequenceGenerator(name = "experiment_id", sequenceName = "experiment_id_seq", allocationSize = 1)

public class Experiment implements DataModel {

    @Id
    @GeneratedValue(strategy = GenerationType.AUTO, generator = "experiment_id")
    private long id;

    @Column(name = "label", length = 128)
    private String label;

    @Column(name = "url", length = 512)
    private String url;

    @Column(name = "owner_email", length = 512)
    private String ownerEmail;

    @Column(name = "creation_time")
    private Date creationTime;

    @ManyToMany(fetch = FetchType.LAZY)
    @JoinTable(name = "experiment_entry", joinColumns = {@JoinColumn(name = "experiment_id", nullable = false)},
            inverseJoinColumns = {@JoinColumn(name = "entry_id", nullable = false)})
    @LazyCollection(LazyCollectionOption.EXTRA)
    private Set<org.jbei.ice.storage.model.Entry> subjects = new LinkedHashSet<>();

    public long getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public Date getCreationTime() {
        return creationTime;
    }

    public void setCreationTime(Date creationTime) {
        this.creationTime = creationTime;
    }

    public Set<org.jbei.ice.storage.model.Entry> getSubjects() {
        return subjects;
    }

    public void setSubjects(Set<Entry> subjects) {
        this.subjects = subjects;
    }

    public String getOwnerEmail() {
        return ownerEmail;
    }

    public void setOwnerEmail(String ownerEmail) {
        this.ownerEmail = ownerEmail;
    }

    public Study toDataTransferObject() {
        Study study = new Study();
        study.setId(id);
        study.setCreated(creationTime.getTime());
        study.setLabel(label);
        study.setOwnerEmail(this.ownerEmail);
        study.setUrl(url);
        return study;
    }
}
